
public class StringApp {

	public static void main(String[] args) {
		
		//Character VS String
		System.out.println("Hello World");
		System.out.println('H');
		System.out.println("H");
		
		System.out.println("Hello"+"World");
		
		//New Line
		System.out.println("Hello \nWorld");
		
		//Escape
		System.out.println("Hello \"World\"");
	}

}
